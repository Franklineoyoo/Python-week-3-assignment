#Sets is a collection of unique data.
#the elements cannot be duplicated example "national ID"
studentsIds = {12, 13, 26, 45, 35}

print(studentsIds)

#N/B Sets do not follow a particular order to be printed out as noticed on the terminal. "{35, 45, 26, 12, 13}"
#Sets are unordered or unindexed.
#Sets by default are unordered collection of items. To sort them out you need to use the method "sorted" before printing out the set structure.


print(sorted(studentsIds))












